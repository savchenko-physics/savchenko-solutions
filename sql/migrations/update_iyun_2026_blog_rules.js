require('dotenv').config();
const { Pool } = require('pg');

const pool = new Pool({
    user: process.env.PG_USER,
    host: process.env.PG_HOST,
    database: process.env.PG_DATABASE,
    password: process.env.PG_PASSWORD,
    port: process.env.PG_PORT,
    ssl: { rejectUnauthorized: process.env.PG_SSL_REJECT_UNAUTHORIZED === 'true' },
});

const SLUG = 'iyun-2026-konkurs-mercha';

const RULES_SECTION = `

---

## Правила начисления баллов. Полная версия от 8 июня

**Базовый принцип.** Балл начисляется за решённую задачу, а не за правку и не за языковую версию. Одна задача Савченко (например 7.3.6) считается одной единицей, сколько бы раз вы её ни редактировали и на скольких бы языках ни оформляли.

**1. Что считается одним зачётным решением.** Зачётная единица это пара (номер задачи, автор). Если вы впервые публикуете полное решение задачи 7.3.6, вы получаете баллы один раз. Все последующие правки этой же задачи этим же автором баллов не добавляют. Полировка, исправление опечаток, добавление пояснений — это нормальная и желанная работа, но она не начисляет новые баллы.

**2. Базовый балл.** Первое полное решение задачи, удовлетворяющее требованиям (LaTeX, оригинальность, наличие вывода и рассуждения), даёт 1.0 балл.

**3. Множители за приоритетные направления.** К базовому баллу применяются множители. Они отражают то, насколько решение ценно для платформы.

| Категория | Множитель |
|---|---:|
| Ранее нерешённая задача (из списка пустых) | ×1.5 |
| Задача из глав 9, 10, 12, 14 (покрытие меньше 50%) | ×1.5 |
| Решение оформлено на английском при первой публикации | ×1.5 |

Множители стакаются мультипликативно. Английское первое решение ранее нерешённой задачи из главы 12 даёт 1.0 × 1.5 × 1.5 × 1.5 = 3.375 балла. Это начисляется один раз, при первой публикации этой задачи этим автором.

**4. Перевод собственного решения на другой язык.** Если вы уже опубликовали решение задачи на одном языке и получили за него баллы, то перевод этого же решения на другой язык даёт фиксированные 0.5 балла. Множители к переводу не применяются. Перевод засчитывается один раз за задачу, независимо от числа языков. То есть нельзя получить 0.5 за английский, потом ещё 0.5 за немецкий на той же задаче. Один перевод собственного решения, один раз 0.5 балла.

**5. Почему перевод стоит 0.5, а не полный множитель.** Когда вы решаете задачу на английском с нуля как первое решение, вы создаёте новый контент для англоязычной аудитории и получаете ×1.5. Когда вы переводите своё уже существующее русское решение, вывод уже сделан, и вы добавляете языковую версию, но не новое решение. Это реальная работа, поэтому 0.5, но не равная оригинальному выводу, поэтому не 1.5. Это устраняет двойной зачёт за одну решённую задачу.

**6. Перевод чужого решения.** Не засчитывается вообще. Ноль баллов. Перевод чужой работы это полезный вклад в платформу, но в рамках конкурса он не входит в зачёт, чтобы баллы получал тот, кто решил задачу, а не тот, кто перевёл.

**7. Задача, у которой уже есть чужое решение на этом языке.** Если кто-то уже опубликовал решение 7.3.6 на русском, а вы публикуете своё альтернативное решение той же задачи на русском, оно засчитывается как базовый балл 1.0 без множителя за нерешённую задачу (задача уже не пустая). Альтернативные решения приветствуются, но множитель за первое покрытие достаётся тому, кто был первым.

**8. Задача, решённая до начала конкурса.** Если вы решили задачу до 1 июня, она не входит в зачёт июньского конкурса. Но если вы переводите своё дореконкурсное решение на английский в течение конкурса, перевод даёт 0.5 балла по правилу 4. Само старое решение баллов не приносит, потому что оно опубликовано до старта.

**9. Незавершённые или неполные решения.** Решение без вывода, без рассуждения, или состоящее только из ответа, не засчитывается. Если вы опубликовали заготовку и баллы не начислились, допишите вывод, и при следующей проверке решение зачтётся. Здесь правка действительно может добавить балл, но только потому что исходная публикация ещё не была зачётным решением.

**10. Проверка.** Я выборочно проверяю решения на соответствие правилам, особенно переводы (правило 4) и наличие реального вывода (правило 9). Если решение не проходит проверку, баллы по нему снимаются. Цель не наказать, а сохранить честность конкурса для всех.

**Дополнение про существующую систему.** Сейчас система автоматически доначисляет баллы за каждую правку и за каждую языковую версию. Это ошибка подсчёта, которую я исправляю. После исправления таблица результатов будет пересчитана по правилам выше. Если у кого-то баллы уменьшатся после пересчёта, это не потеря, а приведение к честному значению. Я предупрежу заранее и объясню каждое изменение лично.
`;

async function run() {
    try {
        const { rows } = await pool.query(
            'SELECT id, content FROM blog_posts WHERE slug = $1',
            [SLUG]
        );

        if (!rows.length) {
            console.error(`Blog post not found: slug="${SLUG}"`);
            process.exit(1);
        }

        const post = rows[0];

        if (post.content.includes('Полная версия от 8 июня')) {
            console.log('Rules section already present. No changes made.');
            process.exit(0);
        }

        const newContent = post.content + RULES_SECTION;
        await pool.query(
            'UPDATE blog_posts SET content = $1, updated_at = NOW() WHERE id = $2',
            [newContent, post.id]
        );

        console.log('Done. Blog post updated successfully.');
        process.exit(0);
    } catch (err) {
        console.error('Migration failed:', err.message);
        process.exit(1);
    } finally {
        await pool.end();
    }
}

run();
